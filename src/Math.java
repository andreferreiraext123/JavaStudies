public class Math {
    public static void main(String[] args){
        int num_1 = 20;
        int num_2 = 10;
        // Sum operation in java uses '+' symbol
        System.out.println("Sum OP:  " + (num_1 + num_2));
        // Subtraction operation in java uses '-' symbol
        System.out.println("Subtraction OP:  " + (num_1 - num_2));
        // Multiplication operation in java uses '*' symbol
        System.out.println("Multiplication OP:  " + (num_1 * num_2));
        // Division operation in Java uses '/' symbol
        System.out.println("Division OP:  " + (num_1 / num_2));
    }
}
